describe('generateServerTs — pre-call validation', () => {
  it('generated server includes _validate helper', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('function _validate(');
  });

  it('_validate checks required params', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('is required');
  });

  it('_validate checks enum values', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('must be one of');
  });

  it('_validate checks integer type', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('must be integer');
  });

  it('call_api calls _validate before the HTTP fetch', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    const validateIdx = src.indexOf('_validate(');
    const fetchIdx = src.indexOf('await fetch(url,');
    expect(validateIdx).toBeGreaterThan(0);
    expect(validateIdx).toBeLessThan(fetchIdx);
  });

  it('returns validation error without making HTTP request', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('Validation failed');
    expect(src).toContain('fix before calling');
  });
});

describe('generateServerTs — response trimming', () => {
  it('includes _trimResponse helper', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('function _trimResponse(');
  });

  it('call_api uses _trimResponse instead of raw JSON.stringify', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('_trimResponse(data)');
  });

  it('_trimResponse handles top-level arrays', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('Array.isArray(data)');
    expect(src).toContain('Array of');
    expect(src).toContain('more items not shown');
  });

  it('_trimResponse handles list-shape responses with data + has_more', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('has_more');
    expect(src).toContain('paginate with');
  });

  it('_trimResponse gives structural summary for large arbitrary objects', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('Large response');
    expect(src).toContain('Top-level structure');
  });

  it('_trimResponse truncates strings at 8000 chars', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    expect(src).toContain('const MAX = 8000');
    expect(src).toContain('truncated');
  });

  it('_trimResponse is declared before server.tool(', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    const trimIdx = src.indexOf('function _trimResponse(');
    const toolIdx = src.indexOf('server.tool(');
    expect(trimIdx).toBeGreaterThan(0);
    expect(trimIdx).toBeLessThan(toolIdx);
  });

  it('generated _trimResponse contains valid backtick template literals (no escaped backticks)', () => {
    const src = generateServerTs(minimalSpec, [], 'openai', 'text-embedding-3-small');
    // Extract _trimResponse body and verify no \\` sequences (which would be invalid TS)
    const start = src.indexOf('function _trimResponse(');
    const end = src.indexOf('\nfunction ', start + 1);
    const trimFn = src.slice(start, end > start ? end : undefined);
    expect(trimFn).not.toContain('\\`');
  });
});

