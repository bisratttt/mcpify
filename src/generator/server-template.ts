/**
 * Generates a 2-tool MCP server:
 *   1. search_docs — semantic search over all indexed endpoints
 *   2. call_api        — dynamic executor: loads endpoint from SQLite, makes the HTTP call
 *
 * This keeps tool count at 2 regardless of how many endpoints the API has,
 * so connecting agents never get their context bloated by hundreds of tool schemas.
 */
