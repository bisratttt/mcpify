function sanitizeId(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');
}

function generateTrimResponseHelper(): string {
  return `function _trimResponse(data: unknown): string {
  const MAX = 8000;

  // Top-level array — count + first item
  if (Array.isArray(data)) {
    const raw = JSON.stringify(data, null, 2);
    if (raw.length <= MAX) return raw;
    return \`Array of \${data.length} items. First item:\\n\${JSON.stringify(data[0], null, 2)}\\n\\n[\${data.length - 1} more items not shown — use filter/search params if available.]\`;
  }

  if (data && typeof data === 'object') {
    const d = data as Record<string, unknown>;

    // List shape: { data: [...], has_more? } — Stripe, many REST APIs
    if (Array.isArray(d['data']) && (d['data'] as unknown[]).length > 0) {
      const list = d['data'] as unknown[];
      const raw = JSON.stringify(data, null, 2);
      if (raw.length <= MAX) return raw;
      const hasMore = d['has_more'] ?? false;
      const lines = [\`\${list.length} items\${hasMore ? ' (has_more: true — paginate with starting_after/page_token)' : ''}\`];
      lines.push(\`First: \${JSON.stringify(list[0])}\`);
      if (list.length > 1) lines.push(\`Last:  \${JSON.stringify(list[list.length - 1])}\`);
      return lines.join('\\n');
    }

    // Large arbitrary object — structural summary of top-level keys
    const raw = JSON.stringify(data, null, 2);
    if (raw.length <= MAX) return raw;
    const lines = [\`Large response (\${(raw.length / 1024).toFixed(1)}KB). Top-level structure:\`];
    for (const [k, v] of Object.entries(d)) {
      if (Array.isArray(v)) {
        lines.push(\`  \${k}: array[\${v.length}]\${v.length ? ' — first: ' + JSON.stringify(v[0]).slice(0, 120) : ''}\`);
      } else if (v && typeof v === 'object') {
        lines.push(\`  \${k}: object{keys: \${Object.keys(v as object).join(', ')}}\`);
      } else {
        lines.push(\`  \${k}: \${JSON.stringify(v)}\`);
      }
    }
    return lines.join('\\n');
  }

  // Primitive or string fallback
  const raw = String(data);
  return raw.length <= MAX ? raw : raw.slice(0, MAX) + \`\\n[truncated — \${raw.length - MAX} more chars]\`;
}`;
}
